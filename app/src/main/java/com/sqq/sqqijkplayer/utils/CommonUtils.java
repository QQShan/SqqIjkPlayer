    /**
     * 保存当期时间
     * @param context
     * @param time 当期时间
     */
    public static void saveLoadTime(Context context, long time)
    {
        SharedPreferences share = context.getSharedPreferences("loaddataTime", context.MODE_PRIVATE);
        SharedPreferences.Editor edit = share.edit(); //编辑文件
        edit.putLong("time", time);
        edit.commit();
    }

    public static long getLastLoadTime(Context context)
    {
        SharedPreferences share = context.getSharedPreferences("loaddataTime", context.MODE_PRIVATE);
        return share.getLong("time", 0);
    }

    /**
     * 检查是否超过30min
     * @param context
     * @return true:超过 false:没有
     */
    public static boolean checkTimeOut(Context context)
    {
        long time = System.currentTimeMillis();
        long oldTime = getLastLoadTime(context);
        if(time - oldTime > 120 * 1000 * 60)
        {
            saveLoadTime(context, time);
            return true;
        }
        else
        {
            return false;
        }
    }

    public static int dip2px(Context context, float dipValue) {
        final float scale = context.getResources().getDisplayMetrics().density;
        return (int) (dipValue * scale + 0.5f);
    }
